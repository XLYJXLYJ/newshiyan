					<p class="bd_red bge pd10 mgb0 cp_title" style="border-top: none">3.有问题该向哪一级机关信访举报？是不是机关级别越高信访举报效果越好？</p>
					<div class="bd_red pd10 cp_detail" style="border-top: none">
						信访举报事项原则上由事发地党组织或政府解决，事发地党组织或政府解决不了的，也可以由其上一级党组织或政府解决。有些人一开始就到市里、区里越级上访，认为处理效果会更好，但对应由基层党组织或政府处理的信访事项，上级机关一般仍会按照分级负责的原则转下级机关处理。越级上访反而会延长处理时间，贻误处理时机。
					</div>
				</div>
				<div class="cp_btn">
					<p class="bd_red bge pd10 mgb0 cp_title" style="border-top: none">4.是否向多个机关信访举报才能获得特别的重视？</p>
					<div class="bd_red pd10 cp_detail" style="border-top: none">

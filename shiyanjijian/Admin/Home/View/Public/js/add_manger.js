function ad_info_on(){
	var oGrey = document.getElementById('ad_grey');
	var oAd_info = document.getElementById('ad_info');
	oGrey.style.display = "block";
	oAd_info.style.display = "block";
};
function ad_info_off(){
	var oGrey = document.getElementById('ad_grey');
	var oAd_info = document.getElementById('ad_info');
	oGrey.style.display = "none";
	oAd_info.style.display = "none";
};